import java.io.*;
import java.util.*;
import java.math.*;
public class Main
{
	public static void main(String[] args)throws Exception
	{
		BufferedReader in=new BufferedReader(new InputStreamReader(System.in));
		while(true)
		{
			String s=in.readLine();
			if(s==null)
				break;
			System.out.println(s);
		}
		
	}
	
	
}